def normalize_member_name(member_name: str) -> str:
    return member_name.strip().replace(" ", "").replace("\u3000", "")


def to_short_member_name(member_name: str) -> str:
    cleaned = member_name.strip().replace("\u3000", " ")
    parts = [part for part in cleaned.split(" ") if part]
    if len(parts) >= 2:
        return "".join(parts[1:])
    return normalize_member_name(member_name)


